/**
 * Get inline SVG markup for a Lucide icon by name.
 * Returns an empty span if the icon is not found.
 */
export function getIconSvg(name: string, className?: string): string {
  // Normalize name: convert PascalCase to kebab-case, lowercase
  const normalized = name
    .replace(/([a-z])([A-Z])/g, '$1-$2')
    .toLowerCase()
    .trim();

  const paths = ICON_PATHS[normalized];
  if (!paths) {
    // Try without hyphens as a fallback
    const simple = normalized.replace(/-/g, '');
    const fallback = Object.entries(ICON_PATHS).find(([k]) => k.replace(/-/g, '') === simple);
    if (fallback) {
      return `${svgOpen(className || '')}${fallback[1]}</svg>`;
    }
    return `<span class="${className || ''}" aria-hidden="true"></span>`;
  }

  return `${svgOpen(className || '')}${paths}</svg>`;
}

/**
 * Get SVG markup for a social media platform.
 * Uses dedicated social icons or falls back to generic icon.
 */
export function getSocialIconSvg(platform: string, className?: string): string {
  const normalized = platform.toLowerCase().trim();
  // Map common platform names to icon names
  const platformMap: Record<string, string> = {
    facebook: 'facebook',
    twitter: 'twitter',
    x: 'twitter',
    instagram: 'instagram',
    linkedin: 'linkedin',
    github: 'github',
    youtube: 'youtube',
    email: 'mail',
    mail: 'mail',
    phone: 'phone',
    whatsapp: 'message-circle',
    telegram: 'send',
    tiktok: 'play',
    pinterest: 'target',
    reddit: 'message-circle',
    discord: 'message-circle',
    dribbble: 'target',
    behance: 'briefcase',
    medium: 'file-text',
    website: 'globe',
    link: 'link',
  };

  const iconName = platformMap[normalized] || normalized;
  return getIconSvg(iconName, className);
}
